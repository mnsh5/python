friends = ["Rolf", "Charlie", "Anna", "Bob", "Jen"]

print("friends[2:4]", friends[2:4])
print("friends[2:]",friends[2:])
print("friends[:4]", friends[:4])

print("friends[:]", friends[:])

print("friends[-3:]", friends[-3:])
print("friends[:-2]", friends[:-2])
print("friends[-3:-1]", friends[-3:-1])

